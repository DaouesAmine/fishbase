        $sql = 'SELECT `autoctr`, `AreaCode`, `SpecCode`, `StockCode`, `Status`, `Entered`, `DateEntered`, `Modified`, `DateModified`, `Expert`, `DateChecked`, `TS` FROM `faoareas` WHERE `AreaCode` = :p0 AND `StockCode` = :p1';
